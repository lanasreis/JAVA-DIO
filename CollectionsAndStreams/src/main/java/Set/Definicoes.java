package Set;

public class Definicoes {
    /*
        Implementações que aprenderemos
            -java.util.HashSet
                *QUANDO UTILIZAR: não é necessário manter uma ordenação
                *ORDENAÇÃO: não é ordenado, e não permite valores repetidos
                *PERFORMANCE: por não ter repetição de valores e não ser ordenado, é a implementação mais performática
            -java.util.TreeSet
                *QUANDO UTILIZAR:  quando é necessário alterar a ordem atraveś do uso de comparators
                *ORDENAÇÃO: mantém ordem e pode ser reordenado
                *PERFORMANCE: é performático para leitura, para modificação tem a necessidade de reordenar sendo mais lentoque o LinkedHashSet
            -java.util.LinkedHashSet
                *QUANDO UTILIZAR: é necessário manter a ordem de inserção dos elementos
                *ORDENAÇÃO: mantem a ordem de inserção dos elementos
                *PERFORMANCE: é a implementação mais lenta por ser necessária manter a ordem

        Por padrão, não garante ordem
        Não permite itens repetidos
        Permite adição e remoção normalmente. NÂO possui busca por item e atualização. Para leitura, apenas navegação
        Não permite mudança de ordenação
    */
}
