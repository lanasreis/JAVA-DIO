package SincronoEAssincrono;

public class Definicao {
    /*
        *SÍNCRONO
            -O processamento síncrono, são todos os processamentos que ocorrem em sequência (sincronia).
            -Os processos são executados em fila.
            -É preciso que um processo termine para que outro processo seja executado.

            Ex: imagine você lavando a louça e de repente você se lembra que tem que fazer uma ligação
            A ligação só poderá ser realizada quando o processo lavar louça for finalizado.

        * ASSÍCRONO
            -Quando dois oumais processos são realizados ao mesmo tempo, é dado o nome de processamento assíncrono.
            -Os processos são realizados simultaneamente sem que um processo necessite que outro termine para ser executado.

           Ex: lavar louça e falar ao telefone ao mesmo tempo.
           Se você não sabe como fazer isso, prenda o telefone entre a cabeça e o ombro e tenha as mãos livres para lavar a louça

    */
}
